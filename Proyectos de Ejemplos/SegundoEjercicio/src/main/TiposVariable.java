package main;

public class TiposVariable {

	public static void main(String[] args) {
		
		byte edad;
		short ano;
		int mes=32;//Declaracion y intanciacion de variable en una misma linea
		int siglo=5;
		
		
		/*la siguiente linea de codigo imprime en la consolo (System,out)
		*/
		System.out.println(siglo);
		
		siglo=36;
		ano=25;
		edad=15;
		
		System.out.println(edad+ " "+ano);
		System.out.println(mes);
		System.out.println(siglo);
	}

}
